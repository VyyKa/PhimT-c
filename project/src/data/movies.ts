export const getMoviesByCategory = (category: string): Movie[] => {
  return movieData.filter(movie => movie.category === category);
};

export const getFeaturedMovie = (): Movie => {
  return movieData.find(movie => movie.isFeatured) || movieData[0];
};

export const searchMovies = (query: string): Movie[] => {
  return movieData.filter(movie => 
    movie.title.toLowerCase().includes(query.toLowerCase()) ||
    movie.genre.some(g => g.toLowerCase().includes(query.toLowerCase()))
  );
};

export const getMovieById = (id: string): Movie | undefined => {
  return movieData.find(movie => movie.id === id);
};

